function formatearPrecio(n) {
  return n.toLocaleString("es-MX", {
    style: "currency",
    currency: "MXN",
    minimumFractionDigits: 0,
  });
}

function estrellas(r) {
  const filled = "★".repeat(Math.round(r));
  const empty = "☆".repeat(5 - Math.round(r));
  return filled + empty + ` (${r.toFixed(1)})`;
}

